interface GeneratedName {
	adj: string;
	noun: string;
	emoji: string;
}

let lastGenerated: GeneratedName | null = null;

export function getRandomName(): GeneratedName {
	let newName: GeneratedName;

	do {
		const adj = adjectives[Math.floor(Math.random() * adjectives.length)];
		const noun = nouns[Math.floor(Math.random() * nouns.length)];
		const emoji = emojis[Math.floor(Math.random() * emojis.length)];

		newName = { adj, noun, emoji };
	} while (
		lastGenerated &&
		newName.adj === lastGenerated.adj &&
		newName.noun === lastGenerated.noun &&
		newName.emoji === lastGenerated.emoji
	);

	lastGenerated = newName;
	return newName;
}
