export interface DaydreamCard {
	id: string;
	background: string;
	title: string;
	line1: string;
	line2: string;
	timestamp: number;
}

export function createCard(previousCard?: DaydreamCard): DaydreamCard {
	let background, title, firstLine, secondLine;

	// Avoid repeating the exact same card
	do {
		background = backgrounds[Math.floor(Math.random() * backgrounds.length)];
		title = titles[Math.floor(Math.random() * titles.length)];
		firstLine = line1[Math.floor(Math.random() * line1.length)];
		secondLine = line2[Math.floor(Math.random() * line2.length)];
	} while (
		previousCard &&
		background === previousCard.background &&
		title === previousCard.title &&
		firstLine === previousCard.line1 &&
		secondLine === previousCard.line2
	);

	return {
		id: crypto.randomUUID(),
		background,
		title,
		line1: firstLine,
		line2: secondLine,
		timestamp: Date.now(),
	};
}
