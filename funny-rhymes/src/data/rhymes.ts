// Helper function to find rhymes for a given word
export function findRhymes(word: string, maxResults: number = 3): string[] {
	if (!word || word.length < 2) return [];

	// Clean the input word
	const cleanWord = word.toLowerCase().replace(/[^a-z]/g, '');
	if (!cleanWord) return [];

	// Try different ending lengths (3-4 characters)
	for (
		let endingLength = Math.min(4, cleanWord.length);
		endingLength >= 2;
		endingLength--
	) {
		const ending = cleanWord.slice(-endingLength);

		if (rhymeGroups[ending]) {
			const matches = rhymeGroups[ending].filter(
				rhyme => rhyme.toLowerCase() !== cleanWord
			);

			if (matches.length > 0) {
				// Shuffle and return random selection
				const shuffled = [...matches].sort(() => Math.random() - 0.5);
				return shuffled.slice(0, maxResults);
			}
		}
	}

	// Fallback: return random words from any group
	const allWords = Object.values(rhymeGroups).flat();
	const shuffled = [...allWords].sort(() => Math.random() - 0.5);
	return shuffled.slice(0, maxResults);
}

